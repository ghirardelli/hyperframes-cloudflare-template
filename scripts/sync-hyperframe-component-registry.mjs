import { execFileSync } from "node:child_process";
import { createHash } from "node:crypto";
import { existsSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "..");
const checkOnly = process.argv.includes("--check");

const HYPERFRAMES_PACKAGE_VERSION = "0.7.21";

const COMPONENTS = [
  {
    id: "app-showcase",
    name: "App Showcase",
    kind: "block",
    installCommand: "npx hyperframes add app-showcase",
    source: {
      url: "https://github.com/heygen-com/hyperframes/tree/main/packages/hyperframes/registry/blocks/app-showcase",
      packageName: "hyperframes",
      packageVersion: HYPERFRAMES_PACKAGE_VERSION,
      revision: `hyperframes@${HYPERFRAMES_PACKAGE_VERSION}`,
    },
    canonicalSnippet:
      '<div data-composition-id="app-showcase" data-composition-src="compositions/app-showcase.html" data-start="0" data-duration="5.5" data-track-index="1" data-width="1920" data-height="1080"></div>',
    durationSec: 5.5,
    width: 1920,
    height: 1080,
    expectedFiles: ["compositions/app-showcase.html"],
  },
];

const CREDENTIAL_PATTERNS = [
  /github_pat_[A-Za-z0-9_]+/,
  /gh[pousr]_[A-Za-z0-9_]+/,
  /x-access-token/i,
  /BEGIN (?:OPENSSH|RSA|EC|DSA) PRIVATE KEY/,
  /\.ssh\//,
  /credential\.helper/i,
  /https:\/\/[^/\s]+@github\.com/i,
  /\/Users\/[^"'\s]+/,
];

function sha256(value) {
  return `sha256:${createHash("sha256").update(value).digest("hex")}`;
}

function assertNoCredentialLeaks(value) {
  const text = JSON.stringify(value);
  for (const pattern of CREDENTIAL_PATTERNS) {
    if (pattern.test(text)) {
      throw new Error(`trusted component registry contains credential-like material: ${pattern}`);
    }
  }
}

function installComponent(component) {
  const workdir = mkdtempSync(join(tmpdir(), "hyperframe-component-registry-"));
  try {
    writeFileSync(
      join(workdir, "package.json"),
      JSON.stringify({ name: "hyperframe-component-registry-sync", private: true }, null, 2),
    );
    execFileSync("npx", ["--yes", `hyperframes@${HYPERFRAMES_PACKAGE_VERSION}`, "add", component.id], {
      cwd: workdir,
      stdio: "pipe",
    });
    return component.expectedFiles.map((path) => {
      const absolutePath = join(workdir, path);
      if (!existsSync(absolutePath)) {
        throw new Error(`${component.id} did not produce expected file ${path}`);
      }
      const content = readFileSync(absolutePath, "utf8");
      if (!content.includes(`hyperframes-registry-item: ${component.id}`)) {
        throw new Error(`${path} is missing the expected registry marker for ${component.id}`);
      }
      return {
        path,
        content,
        contentHash: sha256(content),
      };
    });
  } finally {
    rmSync(workdir, { recursive: true, force: true });
  }
}

function buildRegistries() {
  const fullComponents = COMPONENTS.map((component) => {
    const files = installComponent(component);
    return {
      id: component.id,
      name: component.name,
      kind: component.kind,
      installCommand: component.installCommand,
      source: component.source,
      canonicalSnippet: component.canonicalSnippet,
      durationSec: component.durationSec,
      width: component.width,
      height: component.height,
      files,
    };
  });
  const publicComponents = fullComponents.map((component) => ({
    ...component,
    files: component.files.map(({ path, contentHash }) => ({ path, contentHash })),
  }));
  const base = {
    version: 1,
    generatedAt: "1970-01-01T00:00:00.000Z",
    generator: {
      script: "scripts/sync-hyperframe-component-registry.mjs",
      packageName: "hyperframes",
      packageVersion: HYPERFRAMES_PACKAGE_VERSION,
    },
  };
  return {
    publicRegistry: { ...base, components: publicComponents },
    serverRegistry: { ...base, components: fullComponents },
  };
}

function renderTs(exportName, value) {
  return [
    "/* eslint-disable */",
    "// This file is generated by scripts/sync-hyperframe-component-registry.mjs. Do not edit manually.",
    `export const ${exportName} = ${JSON.stringify(value, null, 2)} as const;`,
    "",
  ].join("\n");
}

function writeOrCheck(path, content) {
  const absolutePath = join(repoRoot, path);
  if (checkOnly) {
    const current = existsSync(absolutePath) ? readFileSync(absolutePath, "utf8") : "";
    if (current !== content) {
      throw new Error(`${path} is out of date; run npm run sync:hyperframe-components`);
    }
    return;
  }
  writeFileSync(absolutePath, content);
}

const { publicRegistry, serverRegistry } = buildRegistries();
assertNoCredentialLeaks(publicRegistry);
assertNoCredentialLeaks(serverRegistry);

writeOrCheck(
  "src/generated/hyperframe-component-registry-public.ts",
  renderTs("trustedHyperframeComponentRegistryPublic", publicRegistry),
);
writeOrCheck(
  "src/generated/hyperframe-component-registry-server.ts",
  renderTs("trustedHyperframeComponentRegistryServer", serverRegistry),
);

console.log(
  `${checkOnly ? "Checked" : "Synced"} ${publicRegistry.components.length} trusted HyperFrames component(s).`,
);
